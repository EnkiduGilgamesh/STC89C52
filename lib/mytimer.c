#include "mytimer.h"

// 
void initT0(int n, unsigned char flag){
    TMOD = 0x01;                     // 0000 0001
    TH0  = (65536 - n) / 256;        // upper 4 bits
    TH1  = (65536 - n) % 256;        // lower 4 bits

    ET0  = 1;
    EA   = 1;
    TR0  = flag;
}